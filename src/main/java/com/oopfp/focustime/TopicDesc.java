package com.oopfp.focustime;

public class TopicDesc {
    private String name;
    private String desc;

    public TopicDesc(String name, String desc) {
        this.name = name;
        this.desc = desc;
    }

    public String getName() {
        return this.name;
    }

    public String getDesc() {
        return this.desc;
    }

}
